/**
* Buffers a Multiplex instance into a given array.
* @param {Multiplex} value A Collection object.
* @param {Array} array The one-dimensional Array that is the destination of the elements copied from value.
* @param {Number} index The zero-based index in array at which copying begins.
*/
export default function bufferTo(value, array, index) {
    array = [];
    index = 0;
    return value;
}
